<script lang="ts" setup>
import { Broker } from '~comm/worker.api';

const MIN_WIDTH = 500
const MIN_HEIGHT = 600

const corner = $ref(null as SVGElement | null)

function onPointerDown (e: PointerEvent) {
  if (corner) {
    corner.onpointermove = resizeWindow;
    corner.setPointerCapture(e.pointerId);
  }
}

function onPointerUp (e: PointerEvent) {
  if (corner) {
    corner.onpointermove = null;
    corner.releasePointerCapture(e.pointerId);
  }
}

function resizeWindow(e: PointerEvent) {
  Broker.resize({
    width: Math.max(MIN_WIDTH, Math.floor(e.clientX + 5)),
    height: Math.max(MIN_HEIGHT, Math.floor(e.clientY + 5))
  })
}
</script>

<template>
  <svg 
    ref="corner"
    id="corner" 
    width="16" 
    height="16" 
    viewBox="0 0 16 16" 
    fill="none" 
    xmlns="http://www.w3.org/2000/svg"
    @pointerdown="onPointerDown"
    @pointerup="onPointerUp"
  >
    <path d="M16 0V16H0L16 0Z" fill="white"/>
    <path d="M6.22577 16H3L16 3V6.22576L6.22577 16Z" fill="#8C8C8C"/>
    <path d="M11.8602 16H8.63441L16 8.63441V11.8602L11.8602 16Z" fill="#8C8C8C"/>
  </svg>
</template>

<style lang="scss">
  #corner {
    position: fixed;
    bottom: 0;
    right: 0;
    cursor: se-resize;
  }
</style>